    /**
     * This method gets the users from the database.
     * 
     * @return temp - This String[] contains all the users of the system.
     */
    private String[] putNames(){
        localUsers = GuiHandler.getInstance().searchLocalUsers(GuiHandler.getInstance().getUser().getId(), "");
        
        String[] temp = new String[localUsers.size()];
        int pos=0;
        for(User u: localUsers)
            temp[pos++] = u.getUsername() + " - " + u.getEmail();
        
        return temp;
    }
    
