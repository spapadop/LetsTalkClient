    /**
     * This method creates a new conversation when the user presses the button
     * (create).
     * 
     * @param evt - event.
     */
    private void createConversationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createConversationButtonActionPerformed
               
        System.out.println("createConversationButton PRESSED!!!");
        String selectedUserName = selectUserToAdd.getSelectedItem().toString();
        
        
        int chat_type;
        String groupName = "";
        boolean happened = false;
        if(groupConversationBox.isSelected()){
            System.out.println("we know its a group chat");
            chat_type = 0;
            groupName = groupNameText.getText();
            if(groupName.equals(""))
                showMessageDialog(null, "Please provide a group name!");
            else{
                System.out.println("Creating a new Group Chat from userID: " + GuiHandler.getInstance().getUser().getId());
                System.out.println(GuiHandler.getInstance().createConversation(GuiHandler.getInstance().getUser().getId(), -10, chat_type,groupName)); //add last parameter groupName
                happened = true;
            }
        } else{
            chat_type = 1;
            for(User u : localUsers){
                if (selectedUserName.equals(u.getUsername() + " - " +u.getEmail())){
                    System.out.println(GuiHandler.getInstance().createConversation(GuiHandler.getInstance().getUser().getId(), u.getId(), chat_type,groupName)); //add last parameter groupName   
                    happened = true;
                    break;
                }
            }
        }
        
        if(happened){
            GuiHandler.getInstance().getGui().updateConversations();
            this.setVisible(false);
        }
    }//GEN-LAST:event_createConversationButtonActionPerformed

